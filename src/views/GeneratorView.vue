        }


        // Парсинг TSV
        parsedRows.value = sheetsData.value.trim().split(/\r?\n/).filter(line => line.trim() !== '').map(line => {
            const cols = line.split('\t')
            return {
                site: cols[0] || '', page: cols[1] || '', semantics: cols[2] || '',
                donor: cols[3] || '', responsible: cols[4] || '', donor_link: cols[5] || '',
                anchor: cols[6] || '', anchor2: cols[7] || '',
                h1: '', additional_queries: '', donor_theme: ''
            }
        })

        // Распределение врачей
        assignedDoctors.value = []
        let lastDocIdx = -1
        const validDoctors = doctors.value.filter(d => d.name || d.spec || d.link)
        const docsToUse = validDoctors.length > 0 ? validDoctors : [{ name: '', spec: '', link: '' }]

        for (let i = 0; i < parsedRows.value.length; i++) {
            if (docsToUse.length === 1) {
                assignedDoctors.value.push(docsToUse[0])
                continue
            }
            let currentDocIdx
            do {
                currentDocIdx = Math.floor(Math.random() * docsToUse.length)
            } while (currentDocIdx === lastDocIdx)
            assignedDoctors.value.push(docsToUse[currentDocIdx])
            lastDocIdx = currentDocIdx
        }

        currentIdx.value = 0
        isGeneratorOpen.value = true
    }

    const copyText = async (text, event) => {
        try {
            await navigator.clipboard.writeText(text)
            const btn = event.target
            const originalText = btn.innerText
            btn.innerText = 'Скопировано!'
            btn.style.color = 'var(--success)'
            setTimeout(() => {
                btn.innerText = originalText
                btn.style.color = ''
            }, 1200)
        } catch (err) {
            console.error('Не удалось скопировать: ', err)
        }
    }

    // --- ВЫЧИСЛЯЕМЫЕ СВОЙСТВА (АВТО-ОБНОВЛЕНИЕ ВЫВОДА) ---

    const currentRow = computed(() => parsedRows.value[currentIdx.value] || {})
    const currentDoctor = computed(() => assignedDoctors.value[currentIdx.value] || {})

    const replacePlaceholders = (template) => {
        const data = currentRow.value
        const doc = currentDoctor.value
        if (!data.site) return ''

        return template
            .replace(/{{САЙТ}}/g, data.site)
            .replace(/{{ПРОДВИГАЕМАЯ_СТРАНИЦА}}/g, data.page)
            .replace(/{{СЕМАНТИКА}}/g, data.semantics)
            .replace(/{{ДОНОР}}/g, data.donor)
            .replace(/{{ОТВЕТСТВЕННЫЙ}}/g, data.responsible)
            .replace(/{{ССЫЛКА_НА_СТАТЬЮ}}/g, data.donor_link)
            .replace(/{{АНКОР}}/g, data.anchor)
            .replace(/{{АНКОР2}}/g, data.anchor2)
            .replace(/{{Название клиники}}/g, clinicName.value)
            .replace(/{{Заголовок--H1}}/g, data.h1 || '')
            .replace(/{{Доп-запросы}}/g, data.additional_queries || '')
            .replace(/{{Тематика-площадки}}/g, data.donor_theme || '')
            .replace(/{{ВРАЧ_ФИО}}/g, doc.name)
            .replace(/{{ВРАЧ_СПЕЦ}}/g, doc.spec)
            .replace(/{{ВРАЧ_ССЫЛКА}}/g, doc.link)
    }

    const output1 = computed(() => replacePlaceholders(templates.value.t1))
    const output2 = computed(() => replacePlaceholders(templates.value.t2))
    const output3 = computed(() => replacePlaceholders(templates.value.t3))
</script>

